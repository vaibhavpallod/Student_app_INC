package v.s.p;

public class Sponsorreturn {
    String textView;
    int imageid;


    public Sponsorreturn(String textView, int imageid) {
        this.textView = textView;
        this.imageid = imageid;
    }
    public String getTextView() {
        return textView;
    }

    public int getImageid() {
        return imageid;
    }




}
